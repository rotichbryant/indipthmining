const $data = reactive({
  isDisabled: false,
  loading: false,
  form: {
    name:    String(),
    email:   String(), 
    subject: String(),
    message: String()
  }
})
const sendMail = () => {
  $mail.send({
    from:    `${$data.form.name} <${$data.form.email}>`,
